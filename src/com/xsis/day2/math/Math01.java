package com.xsis.day2.math;

public class Math01 {
    public static void main(String[] args) {
        //math abs, return number absolute
        int  m = -4;
        double d = -4.5;
        System.out.println("Math ablsolute : "+Math.abs(m));
        System.out.println("Math ablsolute : "+Math.abs(d));

        //math pow untuk mengkalkulasi eksponensial number
        System.out.println("Math pow : "+Math.pow(2,3));

        //math sqrt
        System.out.println("Math sqrt : "+Math.sqrt(4));

        //math randomize
        System.out.println("Math Randomize : "+Math.random());
    }
}
